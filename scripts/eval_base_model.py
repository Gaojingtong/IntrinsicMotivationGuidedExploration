"""
Supplementary Experiment #2 — Base Model Evaluation
=====================================================
Purpose: Evaluate the base LLM (without any RL fine-tuning) on
         GSM8K / Countdown-34 / Countdown-4, to report baseline accuracy
         and disentangle gains from RL training vs. the main exploration method.

Design principles:
- Fully standalone script; does not depend on Ray or the verl training infrastructure.
- Uses vLLM for inference on the base model.
- Calls the existing compute_score functions in verl.utils.reward_score,
  ensuring identical evaluation logic to the training validation loop.
- Evaluation uses format_score=0.0 (accuracy only), matching ValRewardManager.
- The prompt field's content is the complete prompt string (including any system
  message), and is passed directly to vLLM.

Usage:
    bash scripts/eval_base_model.sh

    # Or run directly:
    python scripts/eval_base_model.py \\
        --model_path ./models/Qwen2.5-3B \\
        --data_files gsm8k:./data/gsm8k/test.parquet \\
                     countdown-34:./data/countdown-34/test.parquet \\
                     countdown-4:./data/countdown-4/test.parquet \\
        --max_tokens 1024 \\
        --temperature 1.0 \\
        --num_samples 1

Dependencies: vllm, transformers, pandas, pyarrow
"""

import argparse
import os
import sys
import pandas as pd
from typing import List, Dict, Tuple, Any
import json

# Allow running from project root so that the verl package is importable
_PROJECT_ROOT = os.path.join(os.path.dirname(os.path.abspath(__file__)), '..')
if _PROJECT_ROOT not in sys.path:
    sys.path.insert(0, _PROJECT_ROOT)

from verl.utils.reward_score import gsm8k, countdown


# ---------------------------------------------------------------------------
# Dataset loading
# ---------------------------------------------------------------------------

def load_test_parquet(parquet_path: str) -> pd.DataFrame:
    """
    Load test split from a parquet file generated by data_preprocess scripts.

    Expected parquet schema:
      - 'data_source': str, e.g. 'countdown' or 'openai/gsm8k'
      - 'prompt': list[dict], [{"role": "user", "content": "<full prompt text>"}]
                  The content field already contains the system message and is
                  ready to be passed directly as a vLLM prompt string.
      - 'reward_model': dict, {"style": "rule", "ground_truth": ...}
                        countdown: ground_truth = {"target": int, "numbers": list}
                        gsm8k:     ground_truth = str (numeric string)

    Args:
        parquet_path (str): Path to the parquet file.

    Returns:
        pd.DataFrame: Loaded test data.
    """
    df = pd.read_parquet(parquet_path)
    print(f"  Loaded {len(df)} samples from {parquet_path}")
    return df


def extract_prompt_text(prompt_field: Any) -> str:
    """
    Extract the full prompt string from the prompt field.

    Data format (from data_preprocess):
        prompt = [{"role": "user", "content": "<complete prompt with system message>"}]
    The content field is the complete model input (may include special tokens such as
    <|im_start|>system ...), and can be passed directly to vLLM.

    Args:
        prompt_field: Raw prompt field (list of dict, or str).

    Returns:
        str: Complete prompt text.
    """
    if isinstance(prompt_field, list):
        for msg in prompt_field:
            if isinstance(msg, dict):
                # The content field is the full prompt; take it directly.
                content = msg.get("content", "")
                if content:
                    return content
        return str(prompt_field)
    elif isinstance(prompt_field, str):
        return prompt_field
    else:
        return str(prompt_field)


def extract_ground_truth(reward_model_field: Any) -> Any:
    """
    Extract the ground_truth value from the reward_model field.

    Args:
        reward_model_field: dict with schema {"style": "rule", "ground_truth": ...}

    Returns:
        The ground_truth value (dict for countdown, str for gsm8k).
    """
    if isinstance(reward_model_field, dict):
        return reward_model_field.get('ground_truth', None)
    else:
        return str(reward_model_field)


# ---------------------------------------------------------------------------
# Model inference (vLLM)
# ---------------------------------------------------------------------------

def build_vllm_engine(model_path: str, tensor_parallel_size: int = 1,
                       gpu_memory_utilization: float = 0.9):
    """
    Initialize a vLLM inference engine.

    Args:
        model_path (str): Local path to the HuggingFace model directory.
        tensor_parallel_size (int): Number of GPUs for tensor parallelism.
        gpu_memory_utilization (float): Fraction of GPU memory to allocate.

    Returns:
        vllm.LLM: Initialized inference engine.
    """
    try:
        from vllm import LLM
    except ImportError:
        raise ImportError("vllm is required. Install with: pip install vllm==0.6.3")

    print(f"  Initializing vLLM engine from {model_path} ...")
    llm = LLM(
        model=model_path,
        tensor_parallel_size=tensor_parallel_size,
        gpu_memory_utilization=gpu_memory_utilization,
        dtype="bfloat16",
        enforce_eager=True,       # Consistent with training rollout config
        trust_remote_code=True,   # Required for some model families
        max_model_len=2048,       # prompt (max 256) + response (max 1024) + margin
    )
    print("  vLLM engine ready.")
    return llm


def generate_responses(llm, prompts: List[str], max_tokens: int = 1024,
                        temperature: float = 1.0, num_samples: int = 1) -> List[List[str]]:
    """
    Generate responses for a list of prompts using vLLM.

    Args:
        llm: vLLM LLM instance.
        prompts (List[str]): List of full prompt strings.
        max_tokens (int): Maximum number of generated tokens per response.
                          Should match max_response_length used in RL training.
        temperature (float): Sampling temperature (matches training rollout; 0 = greedy).
        num_samples (int): Number of responses to generate per prompt.

    Returns:
        List[List[str]]: responses[i][j] is the j-th response for the i-th prompt.
    """
    from vllm import SamplingParams

    # vLLM requires temperature > 0; use a very small value to approximate greedy
    actual_temperature = temperature if temperature > 1e-6 else 1e-6

    sampling_params = SamplingParams(
        temperature=actual_temperature,
        top_p=1.0,
        max_tokens=max_tokens,
        n=num_samples,
    )

    print(f"  Generating {len(prompts)} prompts x {num_samples} samples "
          f"(temperature={temperature:.1f}, max_tokens={max_tokens}) ...")

    outputs = llm.generate(prompts, sampling_params)

    # Extract generated text (vLLM returns only the generated portion, not the prompt)
    results = []
    for req_output in outputs:
        sample_texts = []
        for completion in req_output.outputs:
            sample_texts.append(completion.text)
        results.append(sample_texts)
    return results


# ---------------------------------------------------------------------------
# Evaluation logic
# ---------------------------------------------------------------------------

def select_compute_score_fn(data_source: str):
    """
    Select the scoring function for the given data source.
    Mirrors the _select_rm_score_fn logic used in the training reward managers.

    Args:
        data_source (str): Dataset identifier, e.g. 'openai/gsm8k' or 'countdown'.

    Returns:
        callable: compute_score(solution_str, ground_truth, ...) function.
    """
    if data_source == 'openai/gsm8k':
        return gsm8k.compute_score
    elif "countdown" in data_source:
        return countdown.compute_score
    else:
        raise NotImplementedError(f"Unsupported data_source: {data_source}")


def evaluate_dataset(
    llm,
    parquet_path: str,
    dataset_name: str,
    max_tokens: int = 1024,
    temperature: float = 1.0,
    num_samples: int = 1,
) -> Dict[str, Any]:
    """
    Evaluate the base model accuracy on a single test-set parquet file.

    Evaluation is identical to the training ValRewardManager:
    - format_score=0.0 (accuracy only, no format bonus)
    - error_score=0.0
    - solution_str = prompt + response (equivalent to tokenizer.decode(prompt_ids + response_ids))

    Args:
        llm: vLLM inference engine.
        parquet_path (str): Path to the test parquet file.
        dataset_name (str): Human-readable dataset name for logging.
        max_tokens (int): Maximum generated response length.
        temperature (float): Sampling temperature.
        num_samples (int): Number of responses per question (for pass@n).

    Returns:
        Dict: Metrics dict with keys: dataset, total_samples, pass@1, pass@{num_samples}.
    """
    print(f"\n{'='*60}")
    print(f"Evaluating: {dataset_name}")
    print(f"  File:        {parquet_path}")
    print(f"  Temperature: {temperature}")

    # Load dataset
    df = load_test_parquet(parquet_path)

    # Parse fields
    prompts = []
    ground_truths = []
    data_sources = []

    for _, row in df.iterrows():
        prompt_text = extract_prompt_text(row['prompt'])
        prompts.append(prompt_text)
        ground_truths.append(extract_ground_truth(row['reward_model']))
        data_sources.append(str(row.get('data_source', dataset_name)))

    # All samples in a single parquet share the same data_source
    compute_score_fn = select_compute_score_fn(data_sources[0])

    # Generate responses
    all_responses = generate_responses(
        llm=llm,
        prompts=prompts,
        max_tokens=max_tokens,
        temperature=temperature,
        num_samples=num_samples,
    )

    # Compute accuracy
    total = len(prompts)
    correct_pass1 = 0
    correct_any = 0  # pass@num_samples: correct if any response is right

    for i, (prompt, responses, ground_truth) in enumerate(
            zip(prompts, all_responses, ground_truths)):

        # pass@1: evaluate the first response only
        # solution_str = prompt + response, consistent with training sequences_str
        solution_str_1 = prompt + responses[0]
        score_1 = compute_score_fn(
            solution_str=solution_str_1,
            ground_truth=ground_truth,
            error_score=0.0,
            format_score=0.0,   # No format bonus during evaluation (matches ValRewardManager)
        )
        if score_1 >= 1.0:
            correct_pass1 += 1

        # pass@n: at least one response is correct
        any_correct = False
        for resp in responses:
            full_str = prompt + resp
            score_n = compute_score_fn(
                solution_str=full_str,
                ground_truth=ground_truth,
                error_score=0.0,
                format_score=0.0,
            )
            if score_n >= 1.0:
                any_correct = True
                break
        if any_correct:
            correct_any += 1

        # Progress logging every 100 samples
        if (i + 1) % 100 == 0 or (i + 1) == total:
            print(f"  Progress: {i+1}/{total} | "
                  f"pass@1={correct_pass1/(i+1):.4f} | "
                  f"pass@{num_samples}={correct_any/(i+1):.4f}")

    accuracy_pass1 = correct_pass1 / total
    accuracy_passn = correct_any / total

    metrics = {
        'dataset': dataset_name,
        'data_source': data_sources[0],
        'total_samples': total,
        'pass@1': round(accuracy_pass1, 4),
        f'pass@{num_samples}': round(accuracy_passn, 4),
    }

    print(f"\n  === Results for {dataset_name} ===")
    print(f"  Total:  {total}")
    print(f"  pass@1: {accuracy_pass1:.4f}")
    if num_samples > 1:
        print(f"  pass@{num_samples}: {accuracy_passn:.4f}")

    return metrics


# ---------------------------------------------------------------------------
# Argument parsing helpers
# ---------------------------------------------------------------------------

def parse_data_files(data_files_raw: List[str]) -> List[Tuple[str, str]]:
    """
    Parse --data_files argument values of the form "name:path/to/test.parquet".

    Args:
        data_files_raw: Raw argument list.

    Returns:
        List[Tuple[str, str]]: [(dataset_name, parquet_path), ...]
    """
    result = []
    for item in data_files_raw:
        if ':' in item:
            parts = item.split(':', 1)
            result.append((parts[0].strip(), parts[1].strip()))
        else:
            name = os.path.splitext(os.path.basename(item))[0]
            result.append((name, item))
    return result


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description=(
            'Evaluate base model (no RL fine-tuning) accuracy on reasoning datasets. '
            'Reports pass@1 accuracy on GSM8K / Countdown-34 / Countdown-4.'
        )
    )
    parser.add_argument(
        '--model_path', type=str, required=True,
        help='Path to the base model directory (e.g., ./models/Qwen2.5-3B)'
    )
    parser.add_argument(
        '--data_files', type=str, nargs='+', required=True,
        help=(
            'One or more dataset files as "name:path/to/test.parquet". '
            'Example: '
            'gsm8k:./data/gsm8k/test.parquet '
            'countdown-34:./data/countdown-34/test.parquet '
            'countdown-4:./data/countdown-4/test.parquet'
        )
    )
    parser.add_argument(
        '--max_tokens', type=int, default=1024,
        help='Max response tokens (default: 1024, same as RL training)'
    )
    parser.add_argument(
        '--temperature', type=float, default=1.0,
        help='Sampling temperature. 1.0 matches RL training rollout (default: 1.0). Use 0 for greedy.'
    )
    parser.add_argument(
        '--num_samples', type=int, default=1,
        help='Number of responses per question (default: 1 for pass@1)'
    )
    parser.add_argument(
        '--tensor_parallel_size', type=int, default=1,
        help='Number of GPUs for tensor parallelism (default: 1)'
    )
    parser.add_argument(
        '--gpu_memory_utilization', type=float, default=0.9,
        help='vLLM GPU memory utilization ratio (default: 0.9)'
    )
    parser.add_argument(
        '--output_file', type=str, default=None,
        help='Optional: save JSON results to this file'
    )

    args = parser.parse_args()

    print("=" * 60)
    print("Base Model Evaluation")
    print("=" * 60)
    print(f"  Model:       {args.model_path}")
    print(f"  Temperature: {args.temperature}")
    print(f"  Max tokens:  {args.max_tokens}")
    print(f"  Num samples: {args.num_samples}")
    print(f"  TP size:     {args.tensor_parallel_size}")

    dataset_list = parse_data_files(args.data_files)
    print(f"  Datasets ({len(dataset_list)}):")
    for name, path in dataset_list:
        print(f"    {name}: {path}")

    # Initialize vLLM once and reuse across all datasets
    llm = build_vllm_engine(
        model_path=args.model_path,
        tensor_parallel_size=args.tensor_parallel_size,
        gpu_memory_utilization=args.gpu_memory_utilization,
    )

    # Evaluate each dataset
    all_results = []
    for dataset_name, parquet_path in dataset_list:
        if not os.path.exists(parquet_path):
            print(f"\n  WARNING: {parquet_path} not found, skipping {dataset_name}")
            continue

        metrics = evaluate_dataset(
            llm=llm,
            parquet_path=parquet_path,
            dataset_name=dataset_name,
            max_tokens=args.max_tokens,
            temperature=args.temperature,
            num_samples=args.num_samples,
        )
        all_results.append(metrics)

    # Summary table (ready to paste into paper)
    print("\n" + "=" * 60)
    print("SUMMARY — Base Model (No RL Fine-tuning) Accuracy")
    print("=" * 60)
    header = f"  {'Dataset':<20}  {'pass@1':>8}"
    if args.num_samples > 1:
        header += f"  {'pass@' + str(args.num_samples):>10}"
    print(header)
    print("-" * 60)
    for r in all_results:
        row = f"  {r['dataset']:<20}  {r['pass@1']:>8.4f}"
        if args.num_samples > 1:
            key = f"pass@{args.num_samples}"
            if key in r:
                row += f"  {r[key]:>10.4f}"
        print(row)
    print("=" * 60)

    # Optionally save results to JSON
    if args.output_file:
        output_data = {
            'experiment': 'base_model_evaluation',
            'model_path': args.model_path,
            'temperature': args.temperature,
            'max_tokens': args.max_tokens,
            'num_samples': args.num_samples,
            'results': all_results,
        }
        os.makedirs(os.path.dirname(os.path.abspath(args.output_file)), exist_ok=True)
        with open(args.output_file, 'w') as f:
            json.dump(output_data, f, indent=2)
        print(f"\n  Results saved to: {args.output_file}")

    return all_results


if __name__ == '__main__':
    main()
